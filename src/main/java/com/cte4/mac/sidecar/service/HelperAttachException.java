package com.cte4.mac.sidecar.service;

public class HelperAttachException extends Exception {
    private static final long serialVersionUID = -4198590957897574678L;
    public HelperAttachException() {
    }
    public HelperAttachException(String message) {
        super(message);
    }
    public HelperAttachException(Throwable cause) {
        super(cause);
    }
    public HelperAttachException(String message, Throwable cause) {
        super(message, cause);
    }
}
